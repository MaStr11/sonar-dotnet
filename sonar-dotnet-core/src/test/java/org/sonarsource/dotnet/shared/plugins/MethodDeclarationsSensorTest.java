package org.sonarsource.dotnet.shared.plugins;

import java.io.File;
import java.util.Arrays;
import org.junit.Before;
import org.junit.Rule;
import org.junit.Test;
import org.junit.rules.TemporaryFolder;
import org.slf4j.event.Level;
import org.sonar.api.batch.sensor.internal.DefaultSensorDescriptor;
import org.sonar.api.batch.sensor.internal.SensorContextTester;
import org.sonar.api.testfixtures.log.LogTester;
import org.sonarsource.dotnet.shared.plugins.sensors.MethodDeclarationsSensor;

import static org.assertj.core.api.Assertions.assertThat;
import static org.mockito.Mockito.mock;
import static org.mockito.Mockito.when;

public class MethodDeclarationsSensorTest {
  private static final String LANG_NAME = "LANG_NAME";
  private static final File TEST_DATA_DIR = new File("src/test/resources/MethodDeclarationsSensorTest");

  @Rule
  public TemporaryFolder temp = new TemporaryFolder();
  @Rule
  public LogTester logTester = new LogTester();

  private MethodDeclarationsCollector collector;
  private SensorContextTester context;
  private MethodDeclarationsSensor sensor;

  @Before
  public void prepare() {
    logTester.setLevel(Level.DEBUG);
    context = SensorContextTester.create(temp.getRoot());
    PluginMetadata metadata = mock(PluginMetadata.class);
    when(metadata.languageName()).thenReturn(LANG_NAME);
    ModuleConfiguration configuration = mock(ModuleConfiguration.class);
    when(configuration.protobufReportPaths()).thenReturn(
      Arrays.stream(TEST_DATA_DIR.toPath().toFile().listFiles(File::isDirectory)).map(File::toPath).toList());
    collector = new MethodDeclarationsCollector();
    sensor = new MethodDeclarationsSensor(collector, metadata, configuration);
  }

  @Test
  public void should_describe() {
    DefaultSensorDescriptor sensorDescriptor = new DefaultSensorDescriptor();
    sensor.describe(sensorDescriptor);
    assertThat(sensorDescriptor.name()).isEqualTo(LANG_NAME + " Method Declarations");
  }

  @Test
  public void executeMethodDeclarationSensor() {
    sensor.execute(context);
    assertThat(collector.getMethodDeclarations()).satisfiesExactlyInAnyOrder(
      m -> {
        assertThat(m.getFilePath()).endsWith("Calculator\\Calculator.Tests\\obj\\Debug\\net9.0\\Calculator.Tests.AssemblyInfo.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isZero();
      },
      m -> {
        assertThat(m.getFilePath()).endsWith("Calculator\\Calculator.Tests\\obj\\Debug\\net9.0\\.NETCoreApp,Version=v9.0.AssemblyAttributes.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isZero();
      },
      m -> {
        assertThat(m.getFilePath()).endsWith("Calculator\\Calculator.Tests\\obj\\Debug\\net9.0\\Calculator.Tests.GlobalUsings.g.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isZero();
      },
      m -> {
        assertThat(m.getFilePath()).endsWith(".nuget\\packages\\microsoft.net.test.sdk\\17.11.1\\build\\netcoreapp3.1\\Microsoft.NET.Test.Sdk.Program.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isEqualTo(1);
        assertThat(m.getMethodDeclarations(0).getTypeName()).isEqualTo("AutoGeneratedProgram");
        assertThat(m.getMethodDeclarations(0).getMethodName()).isEqualTo("Main");
      },
      m -> {
        assertThat(m.getFilePath()).endsWith("Calculator\\Calculator.Tests\\MSTestSettings.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isZero();
      },
      m -> {
        assertThat(m.getFilePath()).endsWith("Calculator\\Calculator.Tests\\CalculatorTests.cs");
        assertThat(m.getAssemblyName()).isEqualTo("Calculator.Tests");
        assertThat(m.getMethodDeclarationsCount()).isEqualTo(1);
        assertThat(m.getMethodDeclarations(0).getTypeName()).isEqualTo("Calculator.Tests.CalculatorTests");
        assertThat(m.getMethodDeclarations(0).getMethodName()).isEqualTo("TestMethod1");
      }
      );
    assertThat(logTester.logs()).containsExactly("Start importing method declarations.");
  }
}
